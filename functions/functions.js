function cubeNumber(n1){
    return n1**3
}

// console.log(cubeNumber(3));

function maxTwo(n1,n2){
    return n1>n2?n1:n2
}

// console.log(maxTwo(10,20));

function nthDigitMax(n1,n2){
    return n1%10>n2%10?n1:n2
}

